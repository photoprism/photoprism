type FileMap map[string]File

func (m FileMap) Get(name string) File {
	if result, ok := m[name]; ok {
		return result
	}

	return File{FileName: name}
}

func (m FileMap) Pointer(name string) *File {
	if result, ok := m[name]; ok {
		return &result
	}

	return &File{FileName: name}
}

